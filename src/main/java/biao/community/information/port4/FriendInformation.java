package biao.community.information.port4;

public class FriendInformation {
    String uf_id;
    String uf_source;
    boolean uf_blacklist;
    String u_nickname;
    String u_head;
    String u_sex;
    String u_like;
    String u_identity;

    public String getUf_id() {
        return uf_id;
    }

    public void setUf_id(String uf_id) {
        this.uf_id = uf_id;
    }

    public String getUf_source() {
        return uf_source;
    }

    public void setUf_source(String uf_source) {
        this.uf_source = uf_source;
    }

    public boolean isUf_blacklist() {
        return uf_blacklist;
    }

    public void setUf_blacklist(boolean uf_blacklist) {
        this.uf_blacklist = uf_blacklist;
    }

    public String getU_nickname() {
        return u_nickname;
    }

    public void setU_nickname(String u_nickname) {
        this.u_nickname = u_nickname;
    }

    public String getU_head() {
        return u_head;
    }

    public void setU_head(String u_head) {
        this.u_head = u_head;
    }

    public String getU_sex() {
        return u_sex;
    }

    public void setU_sex(String u_sex) {
        this.u_sex = u_sex;
    }

    public String getU_like() {
        return u_like;
    }

    public void setU_like(String u_like) {
        this.u_like = u_like;
    }

    public String getU_identity() {
        return u_identity;
    }

    public void setU_identity(String u_identity) {
        this.u_identity = u_identity;
    }
}
